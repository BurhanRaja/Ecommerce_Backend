const { Schema, default: mongoose } = require("mongoose");

const ParentCategorySchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("ParentCategory", ParentCategorySchema);
